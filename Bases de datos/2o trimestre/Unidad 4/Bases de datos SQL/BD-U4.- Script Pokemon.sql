CREATE TABLE pokemon (
	id_pokemon SMALLINT UNSIGNED PRIMARY KEY AUTO_INCREMENT, -- Número de la pokedex asociado al Pokémon
    nombre VARCHAR(15) NOT NULL,
    evoluciona_de SMALLINT UNSIGNED, -- Indica el Pokémon desde el que evoluciona, caso de tomar el valor NULL no evoluciona desde ninguno
    id_apariencia TINYINT UNSIGNED, -- Diseño del Pokémon
    peso DECIMAL(6,2) NOT NULL, -- En kilogramos
    altura DECIMAL(6,2) NOT NULL, -- En metros
    ps TINYINT UNSIGNED NOT NULL,  -- Puntos de salud: nivel de energía o vitalidad del Pokémon
    ataque TINYINT UNSIGNED NOT NULL, -- Medida de la capacidad ofensiva del Pokémon
    defensa TINYINT UNSIGNED NOT NULL, -- Capacidad para resistir y reducir el daño recibido de los ataques de los oponentes
    especial TINYINT UNSIGNED NOT NULL, -- Poder o capacidad en ataques especiales
    velocidad TINYINT UNSIGNED NOT NULL, -- Determina qué tan rápido actúa en relación con otros Pokémon
    sexo SET('M','H') NOT NULL, -- M: Macho, H: Hembra
    descripcion VARCHAR(300),
    url_imagen VARCHAR(250),
    FOREIGN KEY (evoluciona_de) REFERENCES pokemon(id_pokemon),
    FOREIGN KEY (id_apariencia) REFERENCES apariencia(id_apariencia)
);
